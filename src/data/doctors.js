function makePriceList(basePrice, online) {
  const prices = [
    {
      id: 'primary',
      title: 'Първичен преглед',
      description: 'Преглед, анамнеза и план за лечение',
      price: bgnToEuro(basePrice),
    },
    {
      id: 'secondary',
      title: 'Вторичен преглед',
      description: 'Контролен преглед до 30 дни',
      price: bgnToEuro(Math.max(basePrice - 20, 40)),
    },
    {
      id: 'documents',
      title: 'Преглед на документи',
      description: 'Оценка на резултати и насоки',
      price: bgnToEuro(Math.max(basePrice - 30, 35)),
    },
  ]

  if (online) {
    prices.push({
      id: 'online',
      title: 'Онлайн консултация',
      description: 'Видео разговор през MedLink',
      price: bgnToEuro(Math.max(basePrice - 15, 45)),
    })
  }

  return prices
}

function makeSchedule(id, index, hasFreeSlots, times, basePrice) {
  if (!hasFreeSlots) return []

  return appointmentDays.map((day, dayIndex) => {
    const slots = times
      .filter((_, timeIndex) => (timeIndex + dayIndex + index) % 2 === 0)
      .slice(0, 4)
      .map((time) => ({
        id: `${id}-${dayIndex}-${time}`,
        time,
        price: bgnToEuro(basePrice + (dayIndex % 2) * 5),
      }))

    return {
      id: `${id}-${dayIndex}`,
      ...day,
      slots,
    }
  })
}

function makeDoctor(item, index) {
  const [name, specialty, city, experience, languages, online, rating, reviews] = item
  const id = index + 1
  const slotDays = ['Днес', 'Утре', 'Петък', 'Понеделник', 'Сряда']
  const times = ['09:30', '10:00', '11:30', '13:00', '14:30', '16:30']
  const basePrice = 65 + (index % 6) * 5
  const day = slotDays[index % slotDays.length]
  const time = times[index % times.length]
  const hasFreeSlots = index % 5 !== 2
  const availability = hasFreeSlots
    ? times.slice(index % 3, (index % 3) + 4)
    : []
  const doctorStatus = hasFreeSlots ? 'available' : 'busy'

  return {
    id,
    name,
    specialty,
    city,
    experience,
    languages,
    online,
    status: doctorStatus,
    verificationStatus: 'verified',
    subscriptionStatus: 'active',
    rating,
    reviews,
    image: doctorImages[index % doctorImages.length],
    clinic: `Медицински център MedLink ${city}`,
    address: `ул. Здраве ${12 + id}, ${city}`,
    price: formatEuroPrice(bgnToEuro(basePrice)),
    nextSlot: hasFreeSlots ? `${day}, ${time}` : 'Няма свободни часове',
    priceList: makePriceList(basePrice, online),
    schedule: makeSchedule(id, index, hasFreeSlots, times, basePrice),
    phone: `+359 888 ${String(120000 + id * 137).slice(0, 6)}`,
    bio: `${name} е ${specialty.toLowerCase()} с ${experience} години опит. Работи с внимание към детайла, ясна комуникация и индивидуален план според нуждите на пациента.`,
    services: specialtyServices[specialty] || defaultServices,
    education: [
      `Медицински университет ${city === 'Бургас' ? 'Варна' : city}`,
      `Специалност ${specialty}`,
      'Допълнителни квалификации и ежегодни обучения',
    ],
    availability,
    patientReviews: [
      {
        author: 'Пациент MedLink',
        text: 'Прегледът беше професионален, спокоен и добре организиран.',
      },
      {
        author: 'Потвърден пациент',
        text: 'Получих ясни насоки и внимателно отношение.',
      },
    ],
  }
}

